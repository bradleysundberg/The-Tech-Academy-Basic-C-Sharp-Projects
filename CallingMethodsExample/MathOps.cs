using System;

namespace MathApp
{
    // This class has 3 methods that do math on a number
    public class MathStuff
    {
        // This adds 10 to the number
        public int Add10(int x)
        {
            return x + 10;
        }

        // This multiplies the number by 5
        public int Times5(int x)
        {
            return x * 5;
        }

        // This squares the number
        public int Square(int x)
        {
            return x * x;
        }
    }
}
